function escreverBomDia() {
    let palavra1, palavra2

    palavra1 = 'Bom'
    palavra2 = ' Dia'

    return palavra1 + palavra2
}

export { escreverBomDia }

function juntarNome() {
    let nome1, nome2

    nome1 = 'Letícia'
    nome2 = ' de Sá'

    return nome1 + nome2
}

export { juntarNome }
